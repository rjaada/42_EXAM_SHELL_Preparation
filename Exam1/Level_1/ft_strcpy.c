#include <stdio.h>
#include <unistd.h>

char	*ft_strcpy(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (s1[i] != '\0')
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}

int	main(void)
{
	char *src = "my name is rachid jaada";
	char dst[25] = "";

	ft_strcpy(src, dst);
	printf("%s\n", dst);
	return (0);
}
